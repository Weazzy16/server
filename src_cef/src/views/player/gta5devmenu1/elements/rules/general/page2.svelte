    <p><b>Примечание:</b> диалоги от сообществ по распространению стороннего ПО в различных соц. сетях - не является личной перепиской.</p>

    <p>6. Проверка ресурсов происходит на добровольной основе, администрация не в праве заставлять делать какие-либо действия. <b>| Снятие.</b></p>

