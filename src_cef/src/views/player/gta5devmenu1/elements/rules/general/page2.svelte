    <p>9. Если в ходе проверки были выявлены другие нарушения (продажа / покупка валюты), администрация вправе выдать соответствующие наказание.</p>

    <p>10. Проверка на сторонне ПО не исключает факт наличия / использования стороннего ПО. В случае, если в ходе проверки не было выявлено нарушений, то игрок всё равно может получить игровую блокировку.</p>

    <p>11.1 Администрация проекта не в праве требовать Вас скачать какие-либо файлы. <b>| Снятие.</b></p>
