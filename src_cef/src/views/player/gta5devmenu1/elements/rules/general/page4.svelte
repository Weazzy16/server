    <p>3. Жертва обязана отыгрывать все эмоции, присущие любому человеку в данной ситуации (страх, беспокойство и т.д.). | Demorgan 35 минут.</p>
    <p><b>Примечание:</b> отыгровка крика, боли и прочего может осуществляться как в voice, так и в /do.</p>

    <p>4. Если был похищен лидер / заместитель фракции, либо министр City Hall, то он обязан вести полную видео запись с момента похищения. | Demorgan 90 минут / Устный выговор лидеру.</p>
