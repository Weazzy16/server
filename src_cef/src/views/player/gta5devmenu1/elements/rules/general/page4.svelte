    <p>12.1. Запрещено как-либо пытаться унизить жертву в грубом/эротическом характере. <b>| WARN / Ban 2-5 дней.</b></p>
    <p><b>Пример:</b> заставить петь, кричать, заставить сказать какие-то аморальные вещи, требовать использование неуместной в рамках ограбления / похищения анимации, пытаться унизить с целью розжига OOC конфликта и с целью личного удовлетворения.</p>

