    <p>Сторона выкупа обязана соблюдать все требования со стороны похитителей (предоставить выкуп, возможность отхода и т.д.) | Устный выговор лидеру фракции.</p>
    <p><b>Пояснение:</b> разрешено требовать отмену патруля и зеленый коридор не более, чем на 45 секунд или в радиусе до 750 метров. Отсчет времени идет с момента выезда похитителей с локации, на которой было произведено похищение. Исключение: жертве ничего не угрожает.</p>
