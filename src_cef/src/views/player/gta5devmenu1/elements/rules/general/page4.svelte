    <p><b>Пример:</b> стрельба, враждебная дипломатия, оскорбления от жертвы и т.п.</p>

    <p>13. При похищении / ограблении с жертвой разрешено проводить лишь функциональное взаимодействие. <b>| Demorgan 60-100 минут.</b></p>
    <p><b>Пример:</b> бритье, ограбление, срез бронежилета, парные анимации и т.п.</p>
    <p><b>Примечание:</b> бронежилет разрешено срезать только в том случае, если он надет на жертве.</p>
    <p><b>Исключение:</b> требование передать / отдать содержимое тайника непосредственно на месте его нахождения.</p>
     <p>14. Запрещено совместно похищать / грабить при нейтральной и(или) враждебной дипломатии. <b>| Demorgan 35 минут всем игрокам / Устный выговор лидеру.</b></p>
     <h1>Ограбления</h1>
     <p>1. Ограбления разрешено совершать исключительно через функционал сервера. <b>| Demorgan 35-100 минут / WARN.</b></p>
     <p><b>Пояснение:</b> при грабеже группой лиц (2 и более человека) нельзя повторно грабить человека, сначала один с помощью функционала (например, неудачно), затем второй. Один человек - одна жертва.</p>
     
     <p>2. Запрещено требовать передачу недвижимости/транспорта/денежных средств со счёта в банке/одежду/предметов. <b>| Ban 3 дня.</b></p>
     
     <p>3. Минимальное количество грабителей при ограблении - 2 человека. <b>| Demorgan 15-35 минут.</b></p>
     <p><b>Исключение:</b> в ночное время суток (с 23:00 до 08:00) на территории красных зон, разрешено грабить в одиночку.</p>
     
     <p>4.1. Минимальное количество грабителей для остановки жертвы в движении на транспортном средстве - 4 человека и 2 машины / мотоцикла. <b>| Demorgan 15-35 минут.</b></p>
     <p><b>Исключение:</b> при ограблении полного инкассаторского автомобиля / автомобиля почтальонов (4 человека) достаточно 6 грабителей.</p>
     
     <p>4.2. Ограбление легкового авто на байке осуществляется наличием минимум 2-х мотоциклов. <b>| Demorgan 10-35 минут.</b></p>
     <p><b>Исключение:</b> ограбление тяжелого автомобиля (инкассаторский автомобиль, фура) осуществляется при наличии минимум 4х мотоциклов. <b>| Demorgan 10-35 минут.</b></p>
     
     <p>5. Запрещено ограбление рабочих. <b>| Demorgan 35 минут.</b></p>
     <p><b>Примечание:</b> инкассатор, почтальон, дальнобойщик, мусорщик, водитель автобуса, пожарный. Данное правило работает с 08:00 до 15:00 (МСК).</p>
     
     <p>6. Запрещено оставлять жертву в стяжках / мешке / веревке / скотче. <b>| Demorgan 60 минут всем участникам.</b></p>
     <p><b>Исключение:</b> тяжелораненый, ограбление во время похищения, похищение (в случае, если был получен выкуп от гос. сотрудников и заложники переданы им).</p>
 
